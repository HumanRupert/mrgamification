                        <h2>Client: Kharazmi Broker</h2>
                        <p><strong>Project Type</strong>: Gamified Social Platform</p>
                        <p><strong>Industry</strong>: Financial</p>
                        <p><strong>About</strong>: Kharazmi Social Platform is a platform aiming to increase engagement
                            and loyalty of Kharazmi Broker clients.</p>
                    </div>
                    <div class="col-12 mt-5">
                        <h3>Overview</h3>
                        <p>Aiming to increase user engagement and customer loyalty, Mr. Gamification team designed a
                            social platform for Kharazmi Broker. This social program has had a strong positive impact on
                            all key metrics of Kharazmi Broker, increasing its investments, engagement, and users
                            dramatically.

                        </p>
                    </div>
                    <div class="col-12 col-md-6 mt-5">
                        <h3>The Problem</h3>
                        <p>Many people are hesitant to enter the stock market simply because they don’t know its basics
                            and rules; a set of “tedious” theories behind stock market used to make this obstacle even
                            bigger. Kharazmi Broker lacked an engaging method to teach the newcomers the basic ideas
                            behind the stock market so they can make smart investments, too. They also wanted to
                            increase the engagement of stock market experts, i.e., those who have years of experience in
                            the stock market and/or make significant investments.
                        </p>
                    </div>
                    <div class="col-12 col-md-6 mt-5">
                        <h3>The Method</h3>
                        <p>Mr. Gamification design team, based on the client's needs, divided the users into two
                            categories: Newbies and Masters. We knew that we should find a way to encourage the Masters
                            to mentor the Newbies; in this way, we would be able to increase the Masters' engagement and
                            the Newbies' knowledge at the same time.
                            <br>
                            Then, we decided which gameful elements we are going to use in the platform, based on our
                            audience and our client’s metrics.

                        </p>
                    </div>
                    <div class="col-12 mt-5">
                        <h3>The Platform</h3>
                        <p>The most crucial goal of our design team was to teach the basics of the stock market
                            engagingly and subtly to the users. Thus, we first designed a quest system. Players earn a
                            constant amount of energies at the start of the month, and by accepting every quest, they
                            lose a certain amount of energies (based on quest’s rewards and difficulty), making players
                            extremely conscious about how they spend their energies. The difficulty of every quest
                            depends not only on the task’s complexity but the number of people competing to finish that
                            quest, making players extremely conscious about how other players spend their energies.
                            So far, players are learning how to spend their resources wisely and how to predict how
                            others will spend their resources--two vital skills for every investor. Furthermore, the
                            quests are also about specific stock market principles such as p/e ratio. In other cases,
                            the players need to directly predict future stock prices, which lets them test their
                            forecast skills without actually investing money. This mechanic creates a safe place to
                            learn vital skills.
                        </p>
                    </div>
