                    <div class="col-12 col-md-6 mt-5">
                        <h2>Indipendent Project</h2>
                        <p><strong>Project Type</strong>: Gamified App</p>
                        <p><strong>Industry</strong>: Entertainment</p>
                        <p><strong>About</strong>: Lovemoji is an online dating app that uses users' responses to a list
                            of images to match them.</p>
                    </div>
                    <div class="col-12 mt-5">
                        <h3>Overview</h3>
                        <p>Tired of time-consuming, unengaging dating apps, we decided to design and develop a gamified
                            dating experience. It's been four months since the start of the app's development and
                            design; during this time, we've attracted many local and international investors and
                            achieved two out of four milestones of the project.
                        </p>
                    </div>
                    <div class="col-12 col-md-4 mt-5">
                        <h3>The Problem</h3>
                        <p>Many text-based dating apps can’t make an engaging and fun experience for users. Tedious list
                            of questions, lengthened user biographies, and lack of gamified elements have harmed dating
                            apps’ user experience.</p>
                    </div>
                    <div class="col-12 col-md-8 mt-5">
                        <h3>The Method</h3>
                        <p>First of all, our design team conducted comprehensive user research to find about the
                            demographics of dating app users. We also wanted to know about the lackings of current
                            dating apps and how we can improve their user experience.
                            We noticed two main reasons for disengagement of dating app users: unrewarding experiences
                            and long texts. We need to design a new experience for people who has a narrow attention
                            span and want to experience an engaging experience.
                        </p>
                    </div>
                    <div class="col-12 mt-5">
                        <h3>The App</h3>
                        <p>Most dating apps use unpredictability mechanics as their primary gamified element. However,
                            we used a few more Core Drives to gamify our platform. By using progress bars, experience
                            points, levels, and achievements, we combined the sense of unpredictability with progress.
                            We wanted to make sure that our platform contains both random and fixed rewards. Players can
                            exchange points for Hearts, which allows them to get more results per session.
                            We, then, used the Appointment Dynamics technique: every day at 11 pm, a live dating session
                            starts, longing no more than 5 minutes. After the session, our algorithm, based on users'
                            responses, matches the like-minded. In the beginning, every user gets no more than one match
                            per session, but by spending points and earning Hearts, they get more and more results per
                            session.
                            <br>
                            To increase user loyalty, we implemented a gamification technique, developed by Yu-Kai Chou,
                            called Alfred Effect. As the users participate in more live sessions and finish levels, our
                            algorithm gathers more data about their interests, and it becomes easier to find matches for
                            them. When we collect enough data about a user, we will show her new peers, regardless of
                            her activity. Of course, she can find more matches by participating in live sessions and
                            finish more levels, but getting a constant amount of matches every ten days, based on her
                            past activities, makes it much harder for the user to remove the app from her phone or mind.
                        </p>
                    </div>
